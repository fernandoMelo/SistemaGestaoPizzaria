    private void iniciarCaixa() {
        try {
            lerValorCaixaInicial();
            CaixaBO caixaBO = new CaixaBO();
            caixaBO.abrirCaixa(caixa);
            JOptionPane.showMessageDialog(this, "Caixa iniciado com sucesso!","Caixa iniciado", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } catch (ParseException pe) {
            JOptionPane.showMessageDialog(this, "Valor inv√°lido.", "Abrir caixa", JOptionPane.ERROR_MESSAGE);
        } catch (GestaoPizzariaException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Abrir caixa", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro inesperado! Informe a mensagem de erro ao administrador do sistema", "Abrir caixa", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void lerValorCaixaInicial() throws ParseException {
        String stringValor = txtValorInicial.getText().trim();
        double valor = -1;
        if (stringValor.isEmpty()) {
            valor = 0;
        } else {
            DecimalFormat formatador = new DecimalFormat("#,##0.00");
            valor = formatador.parse(stringValor).doubleValue();
            caixa.setValorInicial(valor);
        }

    }

